#include <unistd.h>

int	ft_str_is_alpha(char	*str)
{
	unsigned int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] < 65 || str[i] > 90) && (str[i] > 122 || str[i] < 97))
			return (0);
		i++;
	}
	return (1);
}
