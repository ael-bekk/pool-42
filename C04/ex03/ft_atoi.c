int	ft_atoi(char *str)
{
	unsigned int	i;
	int				nb;
	int				neg;

	neg = 0;
	nb = 0;
	i = 0;
	while (str[i] && ((str[i] <= 13 && str[i] >= 9) || str[i] == ' '))
		i++;
	while (str[i] && (str[i] == '-' || str[i] == '+'))
	{
		if (str[i] == '-')
			neg++;
		i++;
	}
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	if (neg % 2)
		nb *= -1;
	return (nb);
}
