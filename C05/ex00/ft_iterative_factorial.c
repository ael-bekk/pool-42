int	ft_iterative_factorial(int nb)
{
	int	i;
	int	k;

	k = 1;
	i = 0;
	while (nb > 1)
	{
		k *= nb;
		nb--;
	}
	if (nb >= 0)
		return (k);
	return (0);
}
